# for loop

for x in range(2,10): #it will print from 2 to 9
    print(x) #output is 2,3,4,5,6,7,8,9


# while loop

y = 1
while (y<=4):
    print(y) # output is 1 2 3 4
    y+=1
    print(y) # output is 5